print("KONVERSI SUHU CELCIUS")

celcius = int(input("MASUKKAN NILAI DARI SUHU CELCIUS : "))

print ("hasil konversi dari  derajat celcius ke kelvin adalah" ,  int(celcius) + int(273) ,"K")
print ("hasil konversi dari  derajat celcius ke reamur adalah" , int(4) * (celcius) // int(5) ,"R")
print ("hasil konversi dari  derajat celcius ke fahrenheit adalah" , int(9) * (celcius) // int(5) + int(32) ,"F")

