#Nomor 2
celcius = int(input("masukkan suhu dalam celcius ="))

kelvin = int(celcius + 273)
reamur = int(4/5*celcius)
fahrenheit = int((9/5)*celcius + 32)

print(f"hasil konversi dari suhu {celcius} celcius ke Kelvin = {kelvin}K")
print(f"hasil konversi dari suhu {celcius} celcius ke Reamur = {reamur}R")
print(f"hasil konversi dari suhu {celcius} celcius ke Fahrenheit = {fahrenheit}F")